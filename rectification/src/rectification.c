// - INPUTS: - image: Estructura Image con la informacion de una imagen en particular, proveniente desde la convolucion
// - OUTPUTS: Imagen con su matriz rectificada
// - DESCRIPTION: La rectificacion consiste en tomar una imagen, osea la matriz y verificar cada uno de sus pixeles, si este es un valor negativo se
//                reemplaza su valor por 0, en los demas casos se mantiene el valor del pixel original

void rectification (Image* image) {

    int n, m, current_pixel;

    for (n = 0; n < (image -> height); n++) {

        for (m = 0; m < (image -> width); m++) {
            current_pixel = image -> matrix[n][m];

            if ( current_pixel < 0) {
                image -> matrix[n][m] = 0;
            }
        }
    }
}

