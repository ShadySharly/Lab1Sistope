void rectification (Image* image) {

    int n, m, current_pixel;

    for (n = 0; n < (image -> height); n++) {

        for (m = 0; m < (image -> width); m++) {
            current_pixel = image -> matrix[n][m];

            if ( current_pixel < 0) {
                image -> matrix[n][m] = 0;
            }
        }
    }
}

