# include <stdlib.h>
# include <stdio.h>
# include <unistd.h>
# include <string.h>

# include <sys/types.h>
# include <sys/wait.h>

# include "convolution.h"

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////// MAIN ////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// - INPUTS: - argv[1]: Cantidad de imagenes de entrada
//           - argv[2]: Nombre o ruta del archivo que contiene la mascara
// - OUTPUTS: -
// - DESCRIPTION: Ejecuta la etapa de "convolution" del pipeline, a cada imagen de entrada

int main(int argc, char** argv) {

    int i;
    char* number_images = argv[1];
    char* mask_file_name = argv[2];
    
    for (i = 0; i < atoi(number_images); i++) {
        Image image;
        read(STDIN_FILENO, &image, sizeof(Image));
        Image* convolved_image = convolution (&image, mask_file_name);
        write(STDOUT_FILENO, convolved_image, sizeof(Image));
        
    }
    wait(NULL);
    return 0;
}

